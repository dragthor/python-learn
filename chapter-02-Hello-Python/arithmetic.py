# The result of division always has a decimal point, even if the result is a whole number.

x = 4 / 2

print("result of 4 / 2 is", x) # 2.0

y = 7 / 2

print("result of 7 / 2 is", y) # 3.5

# Integer division.
z = 53 // 24

print("result of 53 // 24 is", z) # 2

# Modulo.

m = 53 % 24

print("result of 53 % 24 is", m) # 5

# Exponentiation.

e = 3 ** 6 # 3 raised to the power of 6.

print("result of 3 ** 6 is", e) # 729